function car_info(manufacturer: string, model: string, ...features: string[]): void {
    let car: {[key: string]: string} = {
        'manufacturer': manufacturer,
        'model': model
    };
    for (let feature of features) {
        let [key, value] = feature.split(':');
        car[key] = value;
    }
    console.log(car);
}
