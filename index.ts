function make_album(artist: string, title: string, tracks?: number): {[key: string]: string | number} {
    let album: {[key: string]: string | number} = {
        'artist': artist,
        'title': title
    };
    if (tracks) {
        album['tracks'] = tracks;
    }
    return album;
}
